    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String fullName= jTextField1.getText();
        String username= jTextField2.getText();
        String email=jTextField3.getText();
        String password=jTextField4.getText();
        String cPassword=jTextField5.getText();
        String employeeId=jTextField6.getText();
        String contact=jTextField11.getText();
      
        if(!password.equals(cPassword)){
            System.out.println("Password doesn't match");
            JOptionPane.showMessageDialog(null,"Password doesn't match" +JOptionPane.INFORMATION_MESSAGE);
        }else{
         LecturerAccount l = new LecturerAccount(fullName, contact ,  email , username ,password ,employeeId);
         LecturerRegisterOperation registerLecturer =new LecturerRegisterOperation();
          registerLecturer.RegisterUser(l.getLecturerId(),  l.getFullName(),l.getContact(), l.getEmail(), l.getUserName(), l.getPassword());
        }


    }//GEN-LAST:event_jButton1ActionPerformed

